import UIKit

/// 动画方向
///
/// - horizontal: 水平
/// - vertical: 垂直
/// - diagonalUp: 对角线往上
/// - diagonalDown: 对角线往下
public enum AnimationDirection {
    /// 水平
    case horizontal
    /// 垂直
    case vertical
    /// 对角线往上
    case diagonalUp
    /// 对角线往下
    case diagonalDown
}
